private void showHoDan() {
        hoDanList = HoDanModify.findAll();

        tableModel.setRowCount(0);

        hoDanList.forEach(hoDan -> {
            tableModel.addRow(new Object[]{tableModel.getRowCount() + 1, hoDan.getIdHo(), hoDan.getSoThanhVien(),
                hoDan.getSoNha(), hoDan.getTaiKhoan(), hoDan.getMatKhau()});
        });
}
