    private void tblHoDanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblHoDanMouseClicked
        int index = tblHoDan.getSelectedRow();

        Admin_ChiTietHoDan chitiet = new Admin_ChiTietHoDan(Integer.parseInt(tableModel.getValueAt(index,0).toString()));

        String idHo =  tableModel.getValueAt(index,0).toString();
        String soNha =  tableModel.getValueAt(index,3).toString();
        String soTV =  tableModel.getValueAt(index,2).toString();
        String taiKhoan =  tableModel.getValueAt(index,3).toString();
        String matKhau =  tableModel.getValueAt(index,4).toString();

        chitiet.setVisible(true);
        chitiet.pack();
        chitiet.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);

        chitiet.txtIdHo.setText(idHo);
        chitiet.txtSoNha.setText(soNha);
        chitiet.txtSoTV.setText(soTV);
        chitiet.txtTaiKhoan.setText(taiKhoan);
        chitiet.txtMatKhau.setText(matKhau);

    }//GEN-LAST:event_tblHoDanMouseClicked

    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        // TODO add your handling code here:
        String input = JOptionPane.showInputDialog(this, "Nhập số nhà muốn tìm kiếm");
        if (input != null && input.length() > 0) {
            hoDanList = HoDanModify.timKiemTheoSoNha(input);

            tableModel.setRowCount(0);

            hoDanList.forEach(hoDan -> {
                tableModel.addRow(new Object[]{tableModel.getRowCount() + 1, hoDan.getIdHo(), hoDan.getSoThanhVien(),
                    hoDan.getSoNha(), hoDan.getTaiKhoan(), hoDan.getMatKhau()});
        });
        } else {
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập số nhà");
            showHoDan();
        }
        if(tableModel.getRowCount()==0){
            JOptionPane.showMessageDialog(this, "Số nhà '"+input+"' không tồn tại");
            showHoDan();
        }
    }//GEN-LAST:event_btnTimKiemActionPerformed

